#대입연산자 작성 요령 : 무언가를 하기 전에 '미리' 작성함
result=50
result+=6 #50+6
print("result += 6 :", result)
result-=6  #56-6
print("result -=6 :", result)
result*=6 #50*6
print("result *= 6 :", result)
result/=6 #300/6
print("result /= 6 :", result)
result//=6 #50//6
print("result //= 6 :", result)
result%=6 #8%6
print("result %= 6 :", result)
result**=6 #2**6
print("result **= 6 :", result)
